package com.oraclecorp.cxshop.facebook.mobile;

public class RESTJSONResponse {
    private String id;
    private String name;
    
    public RESTJSONResponse() {
        super();
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

}
